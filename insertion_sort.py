def insertion_sort(a):
    # Идём по элементам, начиная со второго (индекс 1)
    for i in range(1, len(a)):
        key = a[i]       # текущий вставляемый элемент
        j = i - 1        # индекс для прохода по отсортированной части слева
        # Сдвигаем элементы вправо, пока слева элементы больше key
        while j >= 0 and a[j] > key:
            a[j + 1] = a[j]
            j -= 1
        # Вставляем key в освободившуюся позицию
        a[j + 1] = key

if __name__ == "__main__":
    arr = [12, 11, 13, 5, 6]
    insertion_sort(arr)      # сортируем
    print(" ".join(map(str, arr)))  # вывод результата
